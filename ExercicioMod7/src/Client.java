/**
 * @author kuro
 *
 * Informações do Cliente
 */
public class Client {
    private String name;
    private int mensality;
    private String planType;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPlanType() {
        return planType;
    }

    public void setPlanType(String planType) {
        this.planType = planType;
    }

    public int getMensality() {
        return mensality;
    }

    public void setMensality(int mensality) {
        this.mensality = mensality;
    }


}
